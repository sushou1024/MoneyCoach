const deviceLocale = Localization.getLocales?.()[0]?.languageTag || 'en'
let currentLocale: SupportedLocale = resolveLocale(deviceLocale)

export function resolveLocale(locale: string): SupportedLocale {
  const normalized = locale.toLowerCase()
  if (normalized.startsWith('zh')) {
    if (normalized.includes('hant') || normalized.includes('tw') || normalized.includes('hk')) {
      return 'zh-TW'
    }
    return 'zh-CN'
  }
  if (normalized.startsWith('ja')) return 'ja'
  if (normalized.startsWith('ko')) return 'ko'
  if (normalized.startsWith('en')) return 'en'
  return defaultLocale
}

export function getCurrentLocale() {
  return currentLocale
}

export function setCurrentLocale(locale: SupportedLocale) {
  currentLocale = locale
}

export function translate(locale: SupportedLocale, key: TranslationKey, params?: Record<string, string | number>) {
  const template = translations[locale][key] || translations[defaultLocale][key] || key
  if (!params) return template
  return template.replace(/\{(\w+)\}/g, (_: string, param: string) => String(params[param] ?? `{${param}}`))
}
